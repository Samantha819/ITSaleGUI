    private void DisplaybuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplaybuttonActionPerformed
        // TODO add your handling code here:
        int Desktopsize = myDesktopInfo.size();
        String sDisplay = "";
        
        if(Desktopsize != 0){
            for(int i = 0; i < Desktopsize; i++){
                sDisplay = sDisplay +"\n+++++++++++++ Desktop Information +++++++++++++";
                sDisplay = sDisplay + myDesktopInfo.get(i).getComputerInfo();
            }
        }else{
            JOptionPane.showMessageDialog(null, "There is NO Desktop Information!");
        }
        
        DisplayArea.setText(sDisplay);
    }//GEN-LAST:event_DisplaybuttonActionPerformed

