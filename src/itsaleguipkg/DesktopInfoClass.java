    private void DonebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DonebuttonActionPerformed
        if(CompIDtxt.getText().trim().length() != 0 && Speedtxt.getText().trim().length() != 0 && Ramtxt.getText().trim().length() != 0
                && HDDtxt.getText().trim().length() != 0 && Pricetxt.getText().trim().length() != 0){
            DesktopClass myDesktop = new DesktopClass();          
            myDesktop.sComputerID = String.valueOf(CompIDtxt.getText());
            myDesktop.dProcessorSpeed = Double.parseDouble(Speedtxt.getText());
            myDesktop.RAM = Integer.parseInt(Ramtxt.getText());
            myDesktop.dPrice = Double.parseDouble(Pricetxt.getText());
            myDesktop.Harddisk = Integer.parseInt(HDDtxt.getText());
            myDesktop.Monitor = Monitortxt.getSelectedItem().toString();
            
           JOptionPane.showMessageDialog(this, "Information has been added successfully !", "Done", JOptionPane.CLOSED_OPTION);
           
            myDesktopInfo.add(myDesktop);
            
        }else{
            JOptionPane.showMessageDialog(this, "All the Fields should not be empty!", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_DonebuttonActionPerformed

    
